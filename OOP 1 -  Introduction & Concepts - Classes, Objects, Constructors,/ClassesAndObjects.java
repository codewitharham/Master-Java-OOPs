import java.security.Key;

public class ClassesAndObjects {

    /* 
    
    2. Classes

        In OOP, a class is a blueprint or a template for creating objects. It defines the 
        structure and behavior that all objects of that class will have.

        
        Key characteristics of a Class:

        * Attributes (Fields/Variables): These are the data members that define the state 
                                         or properties of an object.


        * Methods (Functions): These are the behaviors or actions that an object can perform.

    */

    // Syntax for defining a Class:

/* 
    accessModifier class ClassName {
        
        // Attributes (Fields/Variables)
        
        dataType attributeName1;
        dataType attributeName2;
        // ...

        
        // Methods
        
        accessModifier returnType methodName1(parameters) {
            // method body
        }

        accessModifier returnType methodName2(parameters) {
            // method body
        }
        // ...
    }
        
    
*/

    /*
     
    3. Objects

        An object is an instance of a class. It is a concrete entity that has state 
        (values for its attributes) and behavior (can perform actions defined by its methods).
        You can create multiple objects from a single class blueprint, each with its own 
        unique set of attribute values.



        How to create an Object:

            In Java, objects are created using the new keyword followed by the class name
            and parentheses (which invoke the constructor).

            ClassName objectName = new ClassName();
      

        Accessing Object Members:

            You access the attributes and methods of an object using the dot (.) operator.

     */
}
